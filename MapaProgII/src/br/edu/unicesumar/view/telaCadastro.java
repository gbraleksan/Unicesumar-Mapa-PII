    private boolean validarCampos(){
        if(campoNome.getText().equals("") || campoLogin.getText().equals("")|| campoSenha.getText().equals("") || campoEmail.getText().equals("")){
            telaErroCadastro erroCadastro = new telaErroCadastro();
            erroCadastro.setVisible(true);
            erroCadastro.setLocationRelativeTo(null);
            return false;
        }
        return true;
    }
    
    private void inserirDados(){
        Cadastro cadastro = new Cadastro();
        cadastro.setNome(campoNome.getText());
        cadastro.setLogin(campoLogin.getText());
        cadastro.setSenha(campoSenha.getText());
        cadastro.setEmail(campoEmail.getText());
        CadastroDAO cadDAO = new CadastroDAO();
        cadDAO.cadastrarUsuario(cadastro);
    }
    
     private void limparTela(){
        campoNome.setText("");
        campoLogin.setText("");
        campoSenha.setText("");
        campoEmail.setText("");
    }

    
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        if(validarCampos()){
            inserirDados();
            limparTela();
            super.dispose();
        }
        
        
    }//GEN-LAST:event_btnCadastrarActionPerformed
    
